package com.bean;

public class Application {
	private int id;
	private String email, phone, website, restaurant, address, estd, pincode, country, restauranttype, description,
			userperiod, imagePath;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getWebsite() {
		return website;
	}

	public void setWebsite(String website) {
		this.website = website;
	}

	public String getRestaurant() {
		return restaurant;
	}

	public void setRestaurant(String restaurant) {
		this.restaurant = restaurant;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getEstd() {
		return estd;
	}

	public void setEstd(String estd) {
		this.estd = estd;
	}

	public String getPincode() {
		return pincode;
	}

	public void setPincode(String pincode) {
		this.pincode = pincode;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getRestauranttype() {
		return restauranttype;
	}

	public void setRestauranttype(String restauranttype) {
		this.restauranttype = restauranttype;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getUserperiod() {
		return userperiod;
	}

	public void setUserperiod(String userperiod) {
		this.userperiod = userperiod;
	}
	public String getImagepath() {
		return imagePath;
	}

	public void setImagepath(String imagePath) {
		this.imagePath = imagePath;
	}
}
